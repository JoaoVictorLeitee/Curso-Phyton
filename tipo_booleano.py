"""
Tipo Booleano

2 constantes, verdadeiro(True), Falso(False)
Sempre maiusculo
"""

ativo = True
print(ativo)


"""
Opweações Básicas
Negação (not), sempre o contrario
"""

print(not ativo)

"""
Ou (or)
Binario depende de dois valores, um ou outro deve ser verdadeiro
"""

True or True #true
True or False #true
False or False #false

"""
E (and)
binario, ambos tem que ser verdadeiro
"""

True and True #true
True and False #false
False and False #true